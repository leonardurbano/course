
public class Matematica {
	public static void main(String[] args) {

		double x = 25.0;
		double y = 2.0;
		double z = 8.0;
		int a = -127;

		System.out.println("A raiz quadrada de " + x + " = " + (Math.sqrt(x)));
		System.out.println("A potencia de " + y + " sobre " + z + " = " + (Math.pow(y, z)));
		System.out.println("O valor absoluto de " + a + " = " + (Math.abs(a)));

	}
}
